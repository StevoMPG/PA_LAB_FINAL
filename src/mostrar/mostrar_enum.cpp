void Mostrar::Enum
(	string const* const _enum
){
	for( int n = 0; _enum[ n ] != ""; n += 1 )
		std::cout
			<< "\t"
			<< ( n + 1 )
			<< ". "
			<< _enum[ n ]
			<< "\n";
}

