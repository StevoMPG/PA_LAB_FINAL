void Mostrar::Opciones
(	Opcion const* const opciones
){
	auto const cantidad = Opcion::Cantidad( opciones );

	for( int n = 0; n < cantidad; n += 1 )
		std::cout
			<< "\t"
			<< ( n + 1 )
			<< ". "
			<< opciones[ n ].titulo
			<< "\n";
}

